#2. Realizar un programa que dibuje un cuadrado en consola con * ,usando bucles .

filas=int(input('Ingrese el numero de filas que quiere dibujar: '))
columnas=int(input('Ingrese el numero de columnas que quiere dibujar: '))

for i in range(1, filas+1):
    for j in range(1,columnas+1):
        print("#",end=" ")
    print(" ")





